For commercial and GA aircraft, 71 and 73 percent of bird strikes, respectively, occurred at or below 500 feet AGL. Above 500 feet AGL, the number of strikes declined by 34 percent for each 1,000-foot gain in height for commercial aircraft, and by 44 percent for GA aircraft. Strikes occurring above 500 feet were more likely to cause damage than strikes at or below 500 feet. The record height for a reported bird strike was 31,300 feet.
